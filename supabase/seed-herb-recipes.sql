insert into public.herb_recipes (
  herb_id,
  title,
  preparation_type,
  ingredients,
  instructions,
  dosage_note,
  safety_note
)
select
  herb_match.id,
  recipes.title,
  recipes.preparation_type,
  recipes.ingredients,
  recipes.instructions,
  recipes.dosage_note,
  recipes.safety_note
from recipes
join lateral (
  select herbs.id
  from public.herbs
  where herbs.slug = any(recipes.slugs)
  order by array_position(recipes.slugs, herbs.slug)
  limit 1
) as herb_match on true;
